export const MESSAGES: Record<AppLanguage, Record<MessageKey, string>> = {
  en: EN_MESSAGES,
  fr: FR_MESSAGES,
  es: ES_MESSAGES,
  de: DE_MESSAGES,
  it: IT_MESSAGES,
  pt: PT_MESSAGES,
}

export function translateMessage(
  language: AppLanguage,
  key: MessageKey,
  params: MessageParams = {},
): string {
  const text = MESSAGES[language]?.[key] ?? MESSAGES[DEFAULT_LANGUAGE][key] ?? key
  return text.replace(/\{(\w+)\}/g, (_, param: string) => String(params[param] ?? ''))
}
